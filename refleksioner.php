                <p class="section__text">
                    Jeg endte med at lave en ny tidsplan, hvor al tekst skulle
                    være færdig inden juleferien (d. 20. dec.), så jeg kunne kode
                    websitet i min juleferie og januar. Det viste sig, at det tog
                    længere tid at skrive teksten, og det pressede mig kun yderligere.
                    Da vi kom til d. 25. december, havde jeg skrevet alt udover mine
                    projekter, og denne tekst. Jeg tog et valg, om at nu skulle websitet
                    laves, og så måtte jeg mangle disse 5 tekster, hvis baby kom før –
                    det var bedre at have alt andet klaret end en masse tekster uden website.
                    Jeg kender trods alt mig selv, og ved at jeg til en vis grad er
                    perfektionist og har høje forventninger til mig selv. Jeg forsøger
                    at holde mig selv op mod ”Hvis jeg kan gøre det bedre, skal jeg gøre et bedre”
                    – det er lidt svært nogle gange at skulle gå på kompromis, når man ved
                    at man kan gøre det bedre.
                </p>
