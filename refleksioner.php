                    jeg absolut skulle kunne overholde. Jeg havde dog ikke lige medregnet
                    at jeg havde en eksamen i ”grafisk facilitering” i den første uge, så
                    den tidsplan holdt ikke.
                </p>
                <p class="section__text">
                    Efter den eksamen, følte jeg mig noget presset pga.
                    Jeg var bagud med en hel uge i forhold til min tidsplan,
                    julen var på vej, fødselsdage, højgravid kæreste. Jeg vidste
                    jeg skulle være meget effektiv i mit arbejde, hvis nu baby valgte
                    at komme i december, frem for midt januar. Lige meget hvor godt jeg
                    planlagde ville jeg ikke kunne planlægge mig ud af en fødsel,
                    det ville få det hele til at skride – samtidig med en kæreste
                    der dagligt sagde at hun tror baby kommer før. Jeg kunne mærke
                    stressen stige, og sov egentlig ikke særlig godt. Derfor tog jeg en dag,
                    hvor jeg slet ikke gjorde noget, spillede lidt computer,
                    sov og gik en tur med min kæreste, for så at komme tilbage
                    med nye øjne og skabe mig et overblik. Jeg fik overblikket
                    over hvor jeg var, hvor jeg skulle hen og kunne lave en ny tidsplan.
