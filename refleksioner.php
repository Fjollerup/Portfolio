            <div class="section__item">
                <header class="section__header">
                    <h3>Struktur & Design</h3>
                </header>
                <p class="section__text">
                    Så vi skulle lave en portfolio, som senere hen skal kunne bruges som opslagsværk.
                    Det første jeg gjorde, var at søge inspiration - det brugte jeg den første dag af min design-fase til. 
                </p>
                <h4>Farver</h4>
                <p class="section__text">
                    Til at starte med ville jeg lave en side, med kraftige farver,
                    men så huskede jeg hvor svært det kan være at balancere mange kraftige farver,
                    hvis man også vil have en kontrolleret stil. Derfor valgte jeg megettidligt i min design-fase at jeg ville have et minimal antal farver:
                </p>
                <ul class="section__list">
                    <li class="list__item"><strong>Bagggrund</strong> hvid</li>
                    <li class="list__item"><strong>Base</strong> #111</li>
                    <li class="list__item"><strong>Accent</strong> #ef0d33</li>
                    <li class="list__item"><strong>Tekst</strong> #111, #000, #fff - alt efter baggrunden</li>
                </ul>
                <h4>Navigation som overlay</h4>
                <p class="section__text">
                    Jeg fandt et website, med en overlay-menu og jeg tænkte,
                    at sådan noget vil jeg også lave. Derfor gik jeg i gang med at
                    designe min navigation, som det første. Umiddelbart stod min navigation i midten,
                    men det ændrede jeg af flere grunde:
                </p>
                <ul class="section__list">
                    <li class="list__item">Jeg ville have større sammenhæng mellem navigation og intro/cover sektion</li>
                    <li class="list__item">Navigation holdes i samme område: lokal navigation står i samme område, som overlay-navigation</li>
                    <li class="list__item">H1+intro-tekst står i midten mod venstre. Modsatte side end overlay-navigation</li>
                    <li class="list__item">H1 er stor, og overlay-navigation er stor, som gør at fokus skifter mellem de 2, alt efter om navigationen er fremme eller ej.</li>
                </ul>
                <h4>Cover/intro sektion</h4>
                <p class="section__text">
                    Navigationen og ”cover” sektionerne, som er det første man ser på
                    alle sider, er helt sikkert hvor mit fokus har været
                    størst i forhold til design og layout. Da jeg søgte inspiration så jeg
                    flere sider, hvor man sagtens kunne have tydeliggjort mange ting, og det
                    ville jeg undgå på min portfolio. Derfor mødes man med en sektion der
                    fylder hele viewporten, som introducerer hver side på en enkelt og pæn
                    måde: en stor overskrift, lille beskrivelse af siden og en navigation
                    til de forskellige punkter.
                </p>
                <p class="section__text">
                    Tanker jeg har gjort mig omkring disse sektioner
                </p>
                <ul class="section__list">
                    <li class="list__item">Meget vigtigt at man ikke er i tvivl om, hvor man befinder sig</li>
                    <li class="list__item">Skal være "let fordøjeligt"</li>
                    <li class="list__item">Være pænt og simpelt</li>
                    <li class="list__item">Det må gerne "poppe" en smule: stor hvid overskrift på mørk baggrund</li>
                </ul>
                <p class="section__text">
                    Jeg ville have overskriften er det første man ser, når man kommer ind
                    på en side, og det synes jeg lykkedes. Jeg har med den røde seperator,
                    lige under overskriften, forsøgt at skabe et ”secondary focal point”,
                    som jeg kalder det, der fører øjnene ned til den korte beskrivelse, og
                    til sidst over på navigationen.
                </p>
                <p class="section__text">
                    Samtidig står sidens titel øverst til venstre - igen for at hjælpe
                    brugeren med at vide hvor de er hele tiden. Den vises når man scroller
                    op og skjules når man scroller ned.
                </p>
                <h4>Koden</h4>
                <p class="section__text">
                    Med hensyn til koden, så er den største ting at nævne at så gav jeg mig
                    i kast med en metode der kaldes BEM – Block__Element—Modifer. Det er en
                    metode, hvorpå man simpelthen giver alt en klasse, og på den måde kontrollerer
                    specificity reglen i css. Det tog lidt tid at få hovedet omkring dette, og den
                    måde jeg har udført det på er heller ikke perfekt, eller helt rigtig alle steder
                    – men det har fungeret som et udgangspunkt. Selvom det har været noget kringlet
                    i starten, så synes jeg det var en god idé, da det gjorde nogle elementer på min
                    side, meget nemmere at kontrollerer, samt det gav mig ny indsigt i css.
                    I forhold til HTML, har jeg forsøgt at gøre denne så semantisk som muligt.
                    Jeg ville gerne have lidt mere tid til at optimere denne del, men
                    der er kun den tid der er.
                </p>
