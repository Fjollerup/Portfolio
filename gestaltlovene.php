            <div class="section__item">
                <header class="section__header">
                    <h3>Lighed (Similarity)</h3>
                </header>
                <p class="section__text">
                    Princippet om lighed angiver at når ting virker til at være ens,
                    så grupperer vi dem. Vi har det også med at tro de har samme funktion
