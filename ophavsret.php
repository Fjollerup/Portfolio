                <p class="section__text">
                    Dette er særligt interessant fra en multimediedesigners perspektiv.
                    Som multimediedesigner skaber man mange visuelle kreationer.
                    Dette kan være både tekster, men i særdeleshed også digitale produktioner
                    i form af tekst, billede, lyd og video, sammensat til websites. Vi råder over
                    ophavsretten på disse kreationer, også selvom vi ikke indsætter et
                    copyright-symbol ©, på websitet.
                </p>
                <p class="section__text">
                    Samtidig skal multimediedesigneren også overholde ophavsretten på andres kreationer,
                    og dertil er det ikke tilladt at benytte et hvilket som helst billede vi finder
                    på nettet, til vores eget arbejde.
                    At kopiere andres kreationer er ikke kun tyveri, men det forringer også websitets
                    muligheder for at rangere højt på diverse søgemaskiner. På denne måde er det
                    direkte skadeligt for én selv. Opdager man at kreationer, som man har ophavsret
                    over, er blevet kopieret har man ret til erstatning og vederlag herfor.
                </p>
                <p class="section__text">
                    Er man freelancer, gælder det stik modsatte i forhold til hvis man er fastansat.
                    Her er udgangspunktet at freelanceren beholder retten til deres kreationer.
                    Af samme grund kan man ikke som virksomhed videresælge fotografier, der er
                    købt af en freelancer. Her er en rettighedsoverførsel nødvendigt, som
                    desuden er ganske normal procedure ved freelance arbejde. For at undgå
                    misforståelse skal en skriftlig aftale på plads.
