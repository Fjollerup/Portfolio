                    <h3>Grundlæggende kommunkation</h3>
                </header>
                <p class="section__text">
                    Den væsentligste kompetence en multimediedesigner har, er at kunne producere et website fra bunden,
                    med brug af de seneste teknologier. Det er både fokuseret på privat såvel som offentlige virksomheder.
                    Det starter altid med en analyse af virksomheden og dens behov. Ud fra dette kan man udrede hvad der skal
                    kommunikeres til hvem, hvorfor, hvornår og hvordan. Produktion planlægges, målgruppe fastlægges,
                    det grafiske udtryk skal designes, og til sidst koder man hjemmesiden så man kan interagere med den.
                </p>
                <p class="section__text">
                    Når man står overfor en kommunikationsopgave, så er det vigtigt at vide hvilken løsning man skal bruge
                    – og hvorfor din løsning er den rigtige. Dertil er det vigtigt at vide hvilke antagelser løsningen bygger på,
                    og for at vide det skal man kende forskellene på de forskellige kommunikationsmodeller. Det er vigtigt at huske,
                    at disse modeller forklarer noget forskelligt ud fra de grundlæggende antagelser. Det kommer nemlig an på perspektiv.
                    Dertil har vi paradigmer, og man er nødt til at vide hvilket paradigme man befinder sig i for at vide hvilken løsning vi skal anvende.
                </p>

                <h4>Paradigmer</h4>
                <p class="section__text">
                    Usability handler om at tage et website, og gøre det mere brugbart, eller anvendeligt.
                    Et website skal opleves brugervenligt og brugbart som muligt.
                    Gennem usability kan man derfor gøre et website mere brugbart for dem, som skal bruge det.
                </p>
