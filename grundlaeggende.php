        <section class="section" id="humanistisk">
            <div class="section__item">
                <header class="section__header">
                    <h3>Det Humanistiske Paradigme</h3>
                </header>
                <p class="section__text">
                    Dette paradigme har modtageren i fokus, der anses for aktiv og medskabende.
                    Formålet er at opnå en dybere forståelse for hvad modtageren oplever. I dette paradigme arbejdes der med bløde data,
                    fordi de bygger på subjektive oplevelser fra modtageren, som så skal tolkes. Når afsender har afsendt et budskab,
                    ophører fortolkningen fra afsenders side, og de overlades til modtageren. Dette paradigme kaldes også interaktionsparadigmet,
                    netop på grund af dette samspil. Afsenders budskab og modtagers forståelse er centralt i dette paradigme.
                    Til indsamling af data anvendes kvalitative tilgang, hvor man går i dybden med få specifikt udvalgte modtagere – f.eks. interviews.
                </p>
                <p class="section__text">
                    Overordnede træk ved det humanistiske paradigme
                </p>
                <ul class="section__list">
                    <li class="list__item">Subjektivitet</li>
                    <li class="list__item">Fortolkning</li>
                    <li class="list__item">
                        <a class="text__link" href="impiri.php#kvalitativ">Kvalitativ tilgang</a></li>
                    <li class="list__item">Indiktion</li>
                    <li class="list__item">Situationsbestemt</li>
                </ul>
                <p class="section__text">
                    Det humanistiske paradigme tror på den omskiftelige verden.
                    Her kan modtageren aktivt og deltagende være medskabende i betydningsskabelsesprocessor,
                    så snart de interagerer med en computer.
                </p>
                <ul class="section__kilde">
