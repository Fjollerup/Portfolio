        <section class="section" id="srsor">
            <div class="section__item">
                <header class="section__header">
                    <h3>S-R / S-O-R</h3>
                </header>
                <p class="section__text">
                    Begge disse modeller bygger på kanyleteorien. Denne teori bygger på, at man kan ”sprøjte” idéer,
                    tanker og holdninger ind i modtageren, og på denne måde styre samfundet. Det er bl.a. medierne,
                    der påvirker os – hvis vi ser for mange voldelige film, vil volden i samfundet stige.
                    Selvom teorien ikke nævnes ved navn, så bruges den stadig i dag, når der diskuteres om voldelige computerspil,
                    påvirker de unge.
                </p>
                <h4>S-R</h4>
                <p class="section__text">
                    ”S” står for stimulus og ”R” står for response. Dette handler om at man afsender et budskab(stimulus),
                    og herefter følger så respons – jo kraftigere budskab jo kraftigere respons.
                    S-R modellens fodfæste kan ses ved alle de reklamer vi bliver udsat for konstant,
                    som også kaldes ”shouting advertising”
                </p>
                <div class="img__perspektiv">
                    <img src="img/kommunikation/sr.png" alt="Perspektiv">
                </div>
                <h4>S-O-R</h4>
                <p class="section__text">
                    I S-O-R, tilføjes O’et for ”organisme”. Her overføres budskabet ikke direkte,
                    men føres gennem en organisme. Ikke alle er ens, og budskabet kan overføres på forskellige måder.
                    Udgangspunktet er dog det samme. Her findes der også en antagelse om at der skal tages hensyn
                    til forskellige grupper. Her bruger man målgruppeanalyser til at finde ud af hvilke hensyn der skal tages.
                </p>
