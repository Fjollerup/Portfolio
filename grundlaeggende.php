<!-- THORLACIUS START -->
        <section class="section" id="thorlacius">
            <div class="section__item">
                <header class="section__header">
                    <h3>Thorlacius' kommunikationsmodel</h3>
                </header>
                <p class="section__text">
                    Thorlacius’ kommunikationsmodel samler de funktionsmæssige og æstetiske aspekter ved webdesign.
                    Denne har til formål at forklarer disse aspekter, og bruges både til produktions- og analysesammenhæng.
                    Den bygger samtidig bro mellem de forskellige fagområder og discipliner,
                    der ligger bag skabelsen af et nyt website.
                </p>
                <img src="img/kommunikation/thorlacius.png" alt="">
                <h4>Faktiske afsender</h4>
                <p class="section__text">
                    Denne står bag den reelle kommunikation og intention. Overdrager en opgaven om kommunikation og er ikke nødvendigvis synlig i produktet.
                </p>
                <h4>Implicitte afsender</h4>
                <p class="section__text">
                    Denne kan til gengæld fremanalyseret gennem billeder, tekst og farver
                    – dette kaldes den ekspressive funktion. Her knyttes også forholdet mellem afsender
                    og modtagers følelser, samt afsenders påvirkning af modtager – Dette kaldes de emotive funktion,
                    og kan aflæses gennem receptionsanalyse.
                </p>
                <h4>Implicitte modtager</h4>              
                <p class="section__text">
                    Den som afsender tiltænker kommunikationen.
                    Denne behøver ikke nødvendigvis at være en del af målgruppen,
                    og kan kun fremanalyseres gennem kommunikationsproduktet. Til det anvendes den konative funktion,
                    hvor man prøver at påvirker modtageres vilje og adfærd – man ønsker at få modtager til at udføre en bestemt handling.
                    Det er typisk i de interaktive funktioner man finder den konative funktion.
                </p>
                <h4>Faktiske modtager</h4>
                <p class="section__text">
                    Kan ikke fremanalyseres gennem produktet, men igennem konkret receptionsanalyse.
                </p>
                <h4>Produktet</h4>
                <p class="section__text">
                    Ofte et website. Denne er centrum af modellen, og dækker både kommunikative og æstetiske funktioner. Denne har 2 dele
                </p>
                <ul class="section__list">
                    <li class="list__item">
                        Den formale funktion respekterer den almene ”gode smag”, og idealerne for hvordan et website skal se ud.
                    </li>
                    <li class="list__item">
                        Den sublime funktion innoverer, og repræsentere det nye
                        og forfriskende måder at arbejde med layout, farvekombinationer og stilarter.
                    </li>
                </ul>
                <ul class="section__kilde">
                    <li><a class="kilde__link" href="">Kommunikation og multimediedesign, s.69.</a></li>
                </ul>
                <h4>Konteksten</h4>
                <p class="section__text">
                    Den sammenhæng websitet indgår i. Denne dækker over ”den situationelle kontekst”
                    og ”den tekstlige kontekst” – altså det teksten siger. Her er også 2 dele:
                </p>
                <ul class="section__list">
                    <li class="list__item">
                        Den referentielle funktion referer til selve indholdet, frem for det æstetiske i et produkt.
                    </li>
                    <li class="list__item">
                        Den intertekstuelle funktion handler om forbindelsen med andre tekster
                        – dette kan være citater og gamle skrifttyper.
                    </li>
                </ul>
                <h4>Mediet</h4>
                <p class="section__text">
                    Forbinder afsender og modtager gennem 2 funktioner:
                </p>
                <ul class="section__list">
                    <li class="list__item">
                        Den fatiske funktion fastholder læser/modtager i situationen – f.eks.
                        Når en simpel animation på computeren indikerer at den tænker.
                    </li>
                    <li class="list__item">
                        Den navigative funktion styrer læsningen gennem websitet – dette sker gennem fast struktur. Dette har forbindelse til <a class="text__link" href="ia.php">informationsarkitektur.</a>
                    </li>
                </ul>
                <h4>Koden</h4>
                <p class="section__text">
                    Denne skal forstås således at afsender og modtager skal kunne forstå hinanden
                    - et website til en dansker, skal ikke stå på kinesisk. Hertil er der 2 funktioner:
                </p>
                <ul class="section__list">
                    <li class="list__item">
                        Den metakommunikative funktion handler om at man træder sprogligt eller visuelt ud af situationen og taler om sig selv.
                    </li>
                    <li class="list__item">
                        Den intersemiotiske funktion trækker på ét kodesystem, til at forklarer et andet kodesystem – f.eks. metaforen ”at kaste lys over sagen.”
                    </li>
                </ul>
