            <div class="section__item">
                <header class="section__header">
                    <h3>Design Thinking</h3>
                </header>
                <p class="section__text">
                    Når man siger design tror rigtig mange, at det handler om at give form til noget,
                    og tage beslutninger om form og funktion. Design er mere end det
                    – når man designer noget, trækker man på den viden man har til at
                    tage de rette beslutninger i forhold til opgave, kunde og målgruppe.
                    Der er ikke noget der er overladt til tilfældighederne.
                </p>
                <p class="section__text">
                    Men det er ikke nok til at nå til den gode løsning.
                    En opgave kan ofte deles op i flere problemstillinger,
                    hvor det ikke er nok at have viden om typografi, webdesign og farver.
                    Disse ting kan man på grov vis kalde supplement til det egentlig design.
                    Disse er redskaberne som man anvender, når man ved hvad man skal med dem
                    – når man har den rette problemstilling.
                </p>
                <p class="section__text">
                    I design thinking udnytter man det fulde spektrum af innovative aktiviteter
                    centreret omkring menneskebaseret design værdier. Dette gør man ved at grave
                    i dybden og virkelig prøve at forstå sin målgruppe ved at observerer og stille
                    de rette spørgsmål, så man på denne måde kan finde ud af hvad folk ønsker og har behov for.
                </p>
                <p class="section__text">
                    Det er hele udgangspunktet – det menneskelige ønske eller behov,
                    som derefter integrerer vores viden om designteori: typografi, farver,
                    webdesign. Inden man overhovedet begynder at få idéer, bør man forstå sin
                    målgruppe, både kulturelt og sammenhængsmæssigt.
                </p>
                <ul class="section__kilde">
