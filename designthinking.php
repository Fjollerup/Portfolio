                    <h3>Divergent & konvergent tænkning</h3>
                </header>
                <p class="section__text">
                    Når man arbejder med design thinking, anvender man også 2 måder at tænke på
                    – divergent og konvergent tænkning.
                </p>
                <ul class="section__list">
                    <li class="list__item"><strong>Divergent tænkning</strong> betyder at man er kreativ og tænker bredt. Det er her man åbner op for flere løsninger – f.eks. brainstorming.</li>
                    <li class="list__item"><strong>Konvergent tænkning</strong> betyder at man indskrænker antallet af mulige løsninger. Man sortere og filtrerer i sine idéer.</li>
                </ul>
                <img src="img/designthinking/thinking.png" alt="Model for divergent og konvergent tænkning i Design Thinking processen.">
                <p class="section__text">
                    Man kommer igennem begge måder at tænke på, 2 gange i processen med design thinking.
                    Processen med design thinking vil typisk se således ud. Man finder i første del det rigtige problem og i anden del finder man den rette løsning.
                </p>
