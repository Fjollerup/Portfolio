<!-- FASER START -->
        <section class="section" id="faser">
            <div class="section__item">
                <header class="section__header">
                    <h3>De 6 faser</h3>
                </header>
                <p class="section__text">
                    Design thinking er delt op i 6 faser, som derefter er delt op i 3 faser.
                </p>
                <img src="img/designthinking/dtmodel.png" alt="Model for Design Thinking">
                <h4>Forståelse</h4>
                <h5>Emphasize</h5>
                <p class="section__text">
                    Dette er fasen hvor man er nysgerrig.
                    Det er her hvor man starter smalt, men bredder ud ved at stille
                    spørgsmål og observerer- research. Det er vigtigt at være nysgerrig
                    i hele denne fase, og samtidig organiserer den viden man tilegner sig.
                </p>
                <h5>Define</h5>
                <p class="section__text">
                    Den viden og data man har indsamlet, er organiseret,
                    og man finder mulige problemstillinger. Her vil man ofte
                    opdage at der er problemstillinger, som man slet ikke har tænkt på
                    – Disse kommer fra folks ønsker og behov, som man ikke kan kende ud
                    fra sin designteori alene.
                </p>
                <h4>Udforskning</h4>
                <h5>Ideate</h5>
                <p class="section__text">
                    o	Her starter innovationen I forhold til løsning.
                    Dette er idé-fasen, hvor man får sine idéer til løsninger.
                    Jo bedre de 2 første faser er udført, jo bedre vil denne fase forløbe,
                    og der vil komme flere idéer. Det er her de uventede vilde og skøre idéer
                    springer ud, fra al den research man lavede i de 2 tidligere faser.
                    Jo flere idéer jo bedre. Total fridom er vigtig i denne fase,
                    da idéer genererer idéer.
                </p>
                <h5>Prototype</h5>
                <p class="section__text">
                    o	Her skaber man en prototype, af sit produkt, service eller system.
                    Det er her vi som multimediedesignere laver wireframes, moodboards og
                    eventuelle fysiske representationer af et website. Det er vigtigt,
                    at det er taktilt - altså noget der er sanseligt eller føleligt.
                    Denne fase kan udføres internt
                </p>
                <h4>Materialisering</h4>
                <h5>Test</h5>
                <p class="section__text">
                    o	Her anvender man eksterne tests.
                    En idé kan sagtens virke sikker når man udvikler den,
                    men det betyder ikke at de fungerer i praksis.
                    Derfor er det vigtigt at teste sin prototype og får afprøvet de idéer man har.
                    Man vil typisk teste over flere iterationer, i forskellige faser af prototyping.
                    Måske er det ikke en anvendelig løsning, måske er det den perfekte løsning.
                    Lever løsningen op til forventningerne, hvad tænker og føler brugeren og
                    løser den overhovedet problemstillingen.
                </p>
                <h5>Implement</h5>
                <p class="section__text">
                    o	Den afsluttende fase. Det er her man implementerer sin løsning,
                    så målgruppen får gavn af arbejdet. 
                </p>
                <ul class="section__kilde">
                    <li><a class="kilde__link" href="https://eadania.mrooms.net/course/view.php?id=1744#section-1">Moodle, "Introduktion til innovationsmetoder"</a></li>
                </ul>
            </div>
        </section>
