        <section class="section" id="retorik">
            <div class="section__item">
                <header class="section__header">
                    <h3>Retorik</h3>
                </header>
                <p class="section__text">
                    Oprindeligt talekunst – et stort og anerkendt, selvstændigt fagområde,
                    som vi kan bruge til vores præsentationer.
                    I denne forbindelse skal tale forstås som en præsentation,
                    og dette har vi en opskrift til delt op i 5 faser.
                </p>
                <img src="img/presentation/retorik.png" alt="Retorik">
                <h4>Inventio</h4>
                <p class="section__text">
                    Dette er forberedelsen, Idéfasen, brainstorming – Hvad skal præsentationen handle om? Og hvad er formålet? Der er 3 forskellige taler.                </p>
                <ul class="section__list">
                    <li class="list__item">
                        <strong>Festtalen</strong> Hylder eller dadler med fokus på nuet
                    </li>
                    <li class="list__item">
                        <strong>Den politiske tale</strong> Skuer fremad i samspil med fremtidige perspektiver
                    </li>
                    <li class="list__item">
                       <strong>Den juridiske tale</strong> Skuer bagud, og vurdere om noget er rigtigt eller forkert
                    </li>
                </ul>
                <p class="section__text">
                    Hvad vil man opnå med sin præsentation og hvor vil man kigge hen?
                </p>
                <h4>Dispositio</h4>
                <p class="section__text">
                    Disponering og organisering. Præcisér din præsentation, og udvælg de vigtige argumenter
                    for at få frembragt dit budskab i en logisk struktur.
                    Hvilken rækkefølge er bedst? Giver det mening? Er den gode historie til stede? Strukturen kan deles op.
                </p>
                <ul class="section__list">
                    <li class="list__item">
                        <strong>Exordium</strong> Indledning
                    </li>
                    <li class="list__item">
                        <strong>Narratio</strong> Fortællingen om sagen, den gode historie
                    </li>
                    <li class="list__item">
                        <strong>Argumentatio</strong> Bevisførelsen, her kan trækkes på Toulmin's argumentationsteori om påstand, belæg og hjemmel.
                    </li>
                    <li class="list__item">
                        <strong>Peroratio</strong> Afslutning
                    </li>
                </ul>
                <h4>Elucutio</h4>
                <p class="section__text">
                    Sprog og stil. Her benyttes også logos, pathos og ethos. Her er også 4 former:
                </p>
                <ul class="section__list">
                    <li class="list__item">
                        <strong>Aptum</strong> Hensigtsmæssig, relevant og tilpasset modtagers forhåndsviden.
                    </li>
                    <li class="list__item">
                        <strong>Puritas</strong> Sproglig korrekt
                    </li>
                    <li class="list__item">
                        <strong>Perspicuitas</strong> Klart og forståeligt
                    </li>
                    <li class="list__item">
                        <strong>Ornatus</strong> Levende, varierende, afstemt stil
                    </li>
                </ul>
