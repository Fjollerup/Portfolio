                <header class="section__header">
                    <h3>Præsentationsteknik</h3>
                </header>
                <p class="section__text">
                    Formidlingsopgaver vil altid tage udgangspunkt i et emne, og det skal præsenteres for en given gruppe.
                    For at præsentere et emne er det vigtigt, at det er veldefineret og veldisponeret, for ikke at fremstå tomt og uklart.
                    Samtidig er det vigtigt at vide, hvem man skal præsentere emnet for – modtagergruppen.
                    Kender man ikke sin modtagergruppe kan det være rigtig svært at tilrettelægge sin præsentation efter deres ønsker og behov.
                </p>
                <p class="section__text">
                    Til dette er der flere modeller for oplægsstruktur,
                    som hjælper med at strukturerer sin præsentation.
                    Disse modeller hjælper med at disponerer stoffet.
                </p>
                <ul class="section__list">
                    <li class="list__item">Sig, hvad du vil sige</li>
                    <li class="list__item">Sig det</li>
                    <li class="list__item">Sig, hvad du lige har sgt</li>
                </ul>
                <p class="section__text">
                    Det første punkt er metakommunikation, som fortæller noget om hvad præsentation handler om
                    – hvad man har tænkt sig at komme ind på. Er man til eksamen kan det lyde således
                    ”Mit oplæg kommer til at handle om kommunikation, nærmere betegnet præsentationsteknik,
                    og hvorfor det er vigtigt”. Dernæst holdes selve oplægget. Til sidst metakommunikeres
                    der igen, hvor man kort og præcist gennemgår hvad man lige har sagt.
                </p>
                <p class="section__text">
                    Som sagt er der nogle modeller til at hjælpe os med dette.
                </p>
