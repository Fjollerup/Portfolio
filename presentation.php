        <section class="section" id="practice">
            <div class="section__item">
                <header class="section__header">
                    <h3>Øvelse og Timing</h3>
                </header>
                <p class="section__text">
                    For at fremføre et oplæg med gennemslagskraft er det vigtigt at få øvet det igennem.
                    Når man øver det igennem for andre, og får formuleret sig højt, giver det nogle
                    andre muligheder for at opfange huller i indholdet, eller den røde tråd.
                    Det er samtidig meget nemmere at huske hvad man vil sige, og hvordan man vil præsentere det,
                    hvis man har øvet det grundigt igennem.
                </p>
                <p class="section__text">
                    Timing er også rigtig vigtigt – man har oftest kun få minutter til sin præsentation.
                    Det er derfor rigtig vigtigt at man bruger tiden rigtig,
                    og på den måde får alt det med som man gerne vil.
                    Selv erfarende oplægsholdere har brug for at øve sit stof igennem,
                    og ikke bare holde sig til noter.
                </p>
                <p class="section__text">
                    Dette hjælper også med farten, så man ikke er får hurtig eller får langsomt
                    – det har den effekt at modtager kan følge med. Har man brug for en pause,
                    så tager man en pause, så kan man selv nå at tænke, og modtager kan absorbere det sagte.
                </p>
