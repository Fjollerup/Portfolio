                    <li class="list__item"><strong>Aktualitet</strong> Er det aktuelt lige nu og her, eller følges der op på noget som allerede cirkulere?</li>
                    <li class="list__item"><strong>Konflikt</strong> Når er der opstår en konflikt, som er værd at grave i.</li>
                    <li class="list__item"><strong>Sensation</strong> Det der fascinerer eller chokere udover det normale.</li>
                </ul>
                <p class="section__text">
                    For at kunne bruge disse rigtigt er det vigtigt at kende sin målgruppe godt.
                    Fører man et website, hvor kommunikationen er baseret på B2C (Business to Consumer),
                    så vil det ikke være væsentligt, eller aktuelt, at skrive om hvordan en producents
                    hovedleverandørs fabrik er udbrændt eller strejker. Er kommunikationen derimod baseret
                    på B2B (Business to Business) er det straks en anden sag, for så kan det jo være at det
                    er væsentligt, eller aktuelt for egen virksomhed.
                </p>
