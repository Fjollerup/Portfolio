<!-- TEKSTWEB START -->
        <section class="section" id="tekstweb">
            <div class="section__item">
                <header class="section__header">
                    <h3>tekster til web</h3>
                </header>
                <p class="section__text">
                    Her handler det om noget helt andet, nemlig læserens opmærksomhed.
                    Det er sjældent at folk læser grundigt på nettet – med undtagelse
                    af blogs og lign. De scanner – eller skimmer – siden og søger specifikt
                    efter noget bestemt information. Derfor er det vigtigt at gøre det så let som muligt,
                    at navigere rundt i informationen. Dette kan bl.a. gøres ved hjælp at interne
                    links på siden, disse kan placeres i teksten (tekstlinks) eller i en navigationsmenu
                    i siden. Samtidig må layoutet ikke være for overvældende – det simple layout
                    fungerer bedst. Det skal noteres at disse links skal give mening,
                    ellers tjener de ikke noget formål.
                </p>
                <p class="section__text">
                    Ting man skal være bevidst om:
                </p>
                <ul class="section__list">
                    <li class="list__item"><strong>Tekstlinks</strong> De skal være præcise. Ikke "se mere her"</li>
                    <li class="list__item"><strong>Modtager</strong> Hvem skal vide hvad?</li>
                    <li class="list__item"><strong>Afsender</strong> Hvordan målretter man sit sprog og budskab til brugeren? - Brug ikke lange og kludrede sætninger</li>
                    <li class="list__item"><strong>Formål og mål</strong> Det overordnede formål med sitet, og hvilke mål er der for besøg på specifikke sider.</li>
                    <li class="list__item"><strong>Eksterne links</strong> Hvem kan jeg link til, og hvem kan link til mig?</li>
                    <li class="list__item"><strong>Krav</strong> Stiller brugerne nogle særlige informationskrav? - Er det nødvendigt at koble op til sociale medier?</li>
                </ul>
