<!-- RESEARCH START -->
        <section class="section" id="research">
            <div class="section__item">
                <header class="section__header">
                    <h3>Fase 1 - Research</h3>
                </header>
                <p class="section__text">
                    Vi gik af en eller anden grund ud fra, at denne side skulle handle
                    om netop denne energidrik - Mad-Croc energy - så vi begyndte at research en masse om
                    lige netop denne. Desværre fandt vi ikke noget, som vi kunne bygge vores onepage på,
                    da der ikke rigtig stod noget om dem hverken på den internationale side,
                    eller den danske – Mange er siderne var simpelthen tomme.
                </p>
                <p class="section__text">
                    Det var så her hvor vi måtte søge til vores underviser og fandt ud af,
                    at vores projekt ikke nødvendigvis behøvede at handle om lige præcis denne
                    energidrik – vores overordnede emne var energidrik.
                </p>
                <p class="section__text">
                    Det førte os til at gå ud og stille en masse spørgsmål til forskellige
                    studerende på skolen, både nogle som drak energidrikke og nogle som ikke
                    gjorde. Vi fandt ret hurtigt ud af, at et gennemgående svar på hvorfor de
                    drikker dem, var simpelthen på grund af ”den gode smag”. Ydermere vidste
                    folk ikke rigtig noget om, hvad energidrikke består, eller hvilke former
                    for effekter ingredienserne har på kroppen. Nogle havde oplevet bivirkninger
                    som hjertebanken, søvnbesvær og en enkelt havde oplevet at besvime.
                </p>
                <p class="section__text">
                    Det fik os lidt i tænkeboksen – vi skulle da lave en informationsside om
                    energidrikke. Det var vi ret enige om, ret hurtigt, så vi gik straks i
                    gang med at research om hvilke ingredienser der er i en energidrik, og
                    hvilke effekter disse eventuelt ville have på kroppen. 
                </p>
                <p class="section__text">
                    Vi kom hurtigt frem til 4 hovedemner, som vi kunne lave nogle sektioner til:
                </p>
                <ul class="section__list">
                    <li class="list__item">Ingredienser</li>
                    <li class="list__item">Effekt</li>
                    <li class="list__item">Bivirkninger</li>
                    <li class="list__item">Efter-effekt</li>
                </ul>
