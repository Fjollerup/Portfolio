                    <li class="list__item">Skabe ny camping-kultur blandt de unge</li>
                    <li class="list__item">Bringe kunstnere tættere på sine fans og omvendt</li>
                    <li class="list__item">man skaber et trygt fællesskab omkring det at være ny campist</li>
                </ul>
