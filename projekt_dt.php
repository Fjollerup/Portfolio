                <header class="section__header">
                    <h3>Fase 1</h3>
                </header>
                <p class="section__text">
                    Denne fase er emphasize fasen, hvor vi skulle samle data og research.
                    Det vi startede med, var at køre nogle brainstorms under emnet
                    ”udlån og reservation”. Der gik ikke længe, så havde vi fyldt en
                    stor tavle + et mindre whiteboard. Det første emne vi lagde os på
                    var ”dania udlån”, da vi tænkte vi kan hive en masse data og lave
                    en masse gode interviews på selve skolen. Efter noget vejledning,
                    fik vi at vide det var for tæt på, og vi endte med at gå med camping
                    i stedet. Det tog lidt tid, da vi ikke følte det var ”vores” emne
                    – det var lidt svært at komme i gang, hvor skulle vi starte og hvor
                    skulle vi hen af - men alligevel gik der ikke længe, så havde vi fyldt
                    tavle+whiteboard ud med brainstorms om camping.
                </p>
                <div class="section__img">
                    <img src="img/projekter/designthinking/brainstorm_camping_stor.png" alt="Emphasize brainstorm, stort whiteboard">
                    <img src="img/projekter/designthinking/brainstorm_camping_lille.png" alt="Emphasize brainstorm, lille whiteboard">
                </div>
                <h4>Interviews</h4>
                <p class="section__text">
                    Det næste skridt var at få fat i nogle mennesker, hvor vi skulle
                    få en masse data og med tiden finde nogle problemstillinger.
                    Det vi gjorde, var at gå på facebook, og finde nogle camping
                    orienterede grupper, hvor vi lagde et opslag op.
                </p>
                <p class="section__text">
                    Det lykkedes os at få fat i et par erfarende campister,
                    desværre ikke nogen nye campister. Selvom vi fik at vide at med
                    de rette spørgsmål kan vi snakke i uendeligheder med folk, var vi
                    dog lidt skeptiske om vi nu kunne det. Til vores overraskelse endte
                    vi med at snakke med disse campister i godt 30 minutter hver, selvom
                    vi lagde ud med max 15 minutter. Vi fik også fat i en campingplads,
                    hvor samtalen næsten varede en time. Vi havde pludselig lært lidt om
                    at være nysgerrige, og hvad emphasize fasen handler om.
                </p>
                <p class="section__text">
                    På trods af at vi ikke fik fat i nogle nye campister,
                    så fik vi i hvert fald en del data i forhold til at finde nogle
                    problemstillinger for campingpladser og erfarende campister.
                    Vores endelig problemstillinger kom til at ligge på, hvordan man
                    trækker unge til camping og sikre at den eksisterende camping-etik respekteres.
                </p>
                <h4>Video afleveringer</h4>
                <p class="section__text">
                    Dette var også helt nyt for os, og en lille smule grænseoverskridende, men lærigt om ikke andet. Her er alle 3 af vores afleverede vidoer til første fase.
