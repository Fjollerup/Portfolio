                <p class="section__text">
                    Med udgangspunkt i vores problemstillinger, så startede vi igen brainstorms.
                    Denne gang handlede det netop om løsninger. Vores endelig løsning i form af en app,
                    blev en event-app, som skulle ændre camping-kulturen, særligt for at trække unge til. 
                </p>
                <div class="section__img"><img src="img/projekter/designthinking/brainstorm_ideer.png" alt="Brainstorm af vores ideer i ideate fasen"></div>
                <p class="section__text">
                    Samtidig ville vi åbne op for at typiske koncerter og events kunne
                    afholdes på campingpladser på en mere intim facon, f.eks. intim-koncerter
                    med et meget begrænset antal deltagere, alt efter mængde af plads på de
                    enkelte campingpladser. Formålet her var at trække unge ud på campingpladser,
                    give dem en god oplevelse og gøre det i rammer, hvor man ikke drikker og sviner,
                    som ved normale koncerter. Her ville man så samles bagefter, og
                    kunstneren ville deltage i hyggen og kunne gå rundt og snakke med sine fans.
                    <br><br>
                    Efter endt ophold, ville man så modtage en rabat-kode,
                    til sit næste non-event besøg på campingpladsen.
                </p>
                <ul class="section__list">
