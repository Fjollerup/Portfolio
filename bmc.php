                <p class="section__text">
                    Forretningsmodeller kan fungere som et kort over en virksomheds,
                    eller organisations strategi. Det vil også hjælpe når der skal skabes nye strategiske alternativer.
                    Disse kan implementeres gennem organisatoriske strukturer, processor og systemer. 
                <img src="img/bmc/bmc.png" alt="Business Modle Canvas">
                <p class="section__text">
                    Det er grundlæggende at forstå de 9 byggesten bag forretningsmodellen.
