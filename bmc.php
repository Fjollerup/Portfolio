<!-- SEGMENTER -->
        <section class="section" id="segmenter">
            <div class="section__item">
                <header class="section__header">
                    <h3>Kundesegmenter</h3>
                </header>
                <p class="section__text">
                    Byggestenen der definerer hvilke forskellige kundegrupper,
                    man sigter efter at nå ud til – mennesker og organisationer.
                </p>
                <p class="section__text">
                    Kunderne er forretningsmodellens levebrød – kernen af hele, som får det til at løbe rundt.
                    Kunderne hver deres behov, og derfor deler man dem ud i forskellige segmenter
                    – segmentet vil oftest have fælles behov, adfærdsmønstre. Man skal have en god
                    forståelse for de forskellige segmenter, så en organisation kan træffe den rette
                    beslutning om hvilket de skal henvende sig til. Når denne beslutning er truffet,
                    kan forretningsmodellen dannes omkring dette segment, med en stærk forståelse for
                    de særlige kundebehov segmentet har.
                </p>
            </div>
        </section>
<!-- TILBUD -->
        <section class="section" id="tilbud">
            <div class="section__item">
                <header class="section__header">
                    <h3>Værditilbud</h3>
                </header>
                <p class="section__text">
                    Byggestenen som beskriver de produkter og ydelser, der har værdi for det enkelte kundesegment.
                </p>
                <p class="section__text">
                    For at nå ud til det valgte kundesegment, er man nødt til at have nogle værditilbud
                    – disse er produkter eller ydelser, som opfylder kundens behov. Nogle produkter er
                    innovative og banebrydende, hvor andre eksisterende produkter har flere funktioner og kvaliteter.
                </p>
                <p class="section__text">
                    Baseret på kundesegmentets behov, kan værdierne være kvantitative – pris og tempo, eller kvalitative – design og kundeoplevelse.
                </p>
            </div>
        </section>
<!-- KANALER -->
        <section class="section" id="kanaler">
            <div class="section__item">
                <header class="section__header">
                    <h3>Kanaler</h3>
                </header>
                <p class="section__text">
                    Byggestenen der beskriver, hvor og hvordan en organisation når ud til sine kunder.
                    Det spiller en stor rolle, hvilke kanaler man benytter for at nå ud til sine kunder.
                </p>
                <p class="section__text">
                    Grænsefladen består af 3 kanaler: Kommunikation, distribution og salg.
                    Disse spiller en stor rolle i kundeoplevelsen. Disse berøringspunkter er
                    afgørende når man vil nå ud til et bestemt kundesegment, så det er igen
                    vigtigt at kende sit kundesegment godt – Kundesegmenter har sine præferencer til,
                    hvordan de ønsker at blive nået.
                </p>
            </div>
        </section>
<!-- RELATIONER -->
        <section class="section" id="relationer">
            <div class="section__item">
                <header class="section__header">
                    <h3>Kunderelationer</h3>
                </header>
                <p class="section__text">
                    Her beskrives der hvilke relationer, der etableres til forskellige kundesegmenter.
                    Man skal afstemme hvilke relationer man ønsker at etablere med det specifikke kundesegment
                    – disse kan være både personlige og automatiserede. Ofte kan kunderelationer være drevet af 3 faktorer:
                </p>
                <ul class="section__list">
                    <li class="list__item">Kundehverving</li>
                    <li class="list__item">Kundefastholdelse</li>
                    <li class="list__item">Mersalg</li>
                </ul>
            </div>
        </section>
<!-- INDTÆGTSTRØMME -->
        <section class="section" id="income">
            <div class="section__item">
                <header class="section__header">
                    <h3>Indtægtstrømme</h3>
                </header>
                <p class="section__text">
                    Denne byggesten repræsenterer de penge en virksomhed generer fra sine kunder.
                </p>
                <p class="section__text">
                    En virksomhed er nødt til at sætte sig og finde ud af, hvor meget hvert kundesegment
                    er villig til at betale for deres produkter. Det er afgørende for at kunne generere en,
                    eller flere indtægtsstrømme fra et kundesegment. Indtægtsstrømme kommer i 2 forskellige typer:
                </p>
                <ul class="section__list">
                    <li class="list__item"><strong>Transaktionsbaserede indtægter</strong> som følge af enkeltstående kundebetalinger</li>
                    <li class="list__item"><strong>Tilbagevendende indtægter</strong> som følge af løbende betalinger for levering af enten et værditilbud til kunderne eller kundesupport efter købet</li>
                </ul>
            </div>
        </section>
<!-- RESOURCER -->
        <section class="section" id="ressourcer">
            <div class="section__item">
                <header class="section__header">
                    <h3>Nøgleressourcer</h3>
                </header>
                <p class="section__text">
                    Disse er de vigtigste, og nødvendige aktiver, som får forretningsmodellen til at virke.                </p>
                <p class="section__text">
                    For at kunne udbyde nogle værditilbud, nå kundesegmenter og opretholde disse relationer
                    gennem forskellige kanaler, så skal der nogle ressourcer til. Det kommer selvfølgelige
                    an på, hvilken type virksomhed man er, for at vide hvilke nøgleressourcer der skal til.
                    Disse kan være fysiske økonomiske, immaterielle eller menneskelige
                    - F.eks. hvis værditilbuddet er et website:
                </p>
                <ul class="section__list">
                    <li class="list__item"><strong>Fysiske</strong> Computere til medarbejdere, hvor de udvikler websitet</li>
                    <li class="list__item"><strong>Immaterielle</strong> Website, som kanal til kunderne</li>
                    <li class="list__item"><strong>Menneskelige</strong> Ansatte til management, kundekontakt og produktion af værditilbud</li>
                    <li class="list__item"><strong>Økonomi</strong> Alt kræver penge, ellers er der f.eks. ingen løn til de ansatte</li>
                </ul>
            </div>
        </section>
<!-- AKTIVITERTER -->
        <section class="section" id="aktiviteter">
            <div class="section__item">
                <header class="section__header">
                    <h3>Nøgleaktiviteter</h3>
                </header>
                <p class="section__text">
                    Denne byggesten består af de vigtigste handlinger og aktiviteter ved en virksomhed,
                    der gør at forretningsmodellen virker.
                <p class="section__text">
                    For at skabe succes, har alle virksomheder nogle handlinger der er nødvendige.
                    Dette gør sig også gældende i forbindelse med at udbyde værditilbud.
                    Ligeledes skal der handles for at nå ud til markeder gennem forskellige kanaler,
                    opretholde kunderelationer og generer indtægtsstrømme.
                </p>
                <p class="section__text">
                    Her varieres der også baseret på virksomhed og kundesegment.
                    For en multimediedesigner kan nøgleaktiviteter være webudvikling,
                    brugerundersøgelser og grafisk arbejde.
                </p>
            </div>
        </section>
<!-- PARTNERE -->
        <section class="section" id="partnere">
            <div class="section__item">
                <header class="section__header">
                    <h3>Nøglepartnere</h3>
                </header>
                <p class="section__text">
                    Her beskrives netværket af leverandører og samarbejdspartnere,
                    der er nødvendige for at få forretningen til at løbe rundt.
                <p class="section__text">
                    Der er mange grunde til at man indgår partnerskab med andre virksomheder
                    – dette kan f.eks. være for at reducere risici og uvisheder. Det er en måde at optimere på,
                    og partnerskaber er ved at blive en fast faktor i mange forretningsmodeller.
                    Der er 4 forskellige typer partnerskaber:
                </p>
                <ul class="section__list">
                    <li class="list__item">Strategiske alliancer - ikke konkurrenter</li>
                    <li class="list__item">"Coopetition" - Sammentræning af cooperation og competition. Samarbejde mellem konkurrenter</li>
                    <li class="list__item">Joint ventures hvor man udvikler nye forretninger</li>
                    <li class="list__item">Køber/Leverandør-relationer for forsyninger</li>
                </ul>
            </div>
        </section>
<!-- OMKOSTNINGER -->
        <section class="section" id="omkostninger">
            <div class="section__item">
                <header class="section__header">
                    <h3>Omkostningsstrukturer</h3>
                </header>
                <p class="section__text">
                    Denne omhandler alle omkostninger der er tilknyttet forretningsmodellen.
                <p class="section__text">
                    Når man opererer med en bestemt forretningsmodel,
                    vil man altid pådrage sig nogle omkostninger.
                    Omkostninger er uundgåeligt, for at kunne skabe værdi, opretholde kunderelationer
                    og generere indtægtsstrømme – Der er et ordsprog der lyder ”Det koster penge, at lave penge”.
                    Omkostninger bør så vidt muligt minimeres, for at øge omsætningen.
                    Nogle forretningsmodeller fokuserer på lave omkostningsstrukturer.
                    Man skelner ofte mellem 2 typer: omkostningsbaseret og værdibaseret.
                </p>
