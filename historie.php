                <header class="section__header">
                    <h3>Internettets Historie</h3>
                </header>
                <p class="section__text">
                    Som al anden historie, så kan vi dele internettets historie op i perioder, på trods af hvor ungt internettet faktisk er.
                    Samtidig er det også vigtigt at skelne mellem internettet og World Wide Web.
                </p>
                <p class="section__text">
                    Som udgangspunkt er det vigtigt at vide, hvad internettet faktisk er og hvor det kommer fra.
                </p>
                <p class="section__text">
                    I 1957 kunne en computer kun arbejde på én opgave ad gangen, dette kaldte man batch processing.
                    Men som computerne blev større var det også nødvendigt at opbevarer dem i nogle afkølede rum,
                    hvor man kunne arbejde på dem direkte.
                    Der var rigtig mange spildtid og spildt arbejde ved at man ikke kunne arbejde direkte på computerne, og det medførte rigtig mange fejl.
                    Det var her behovet for en fjernforbindelse opstod.
                </p>
                <p class="section__text">
                    Det første man kom frem til, var time-sharing, hvor flere brugere kunne dele processor kraften fra den samme computer.
                </p>
                <p class="section__text">
                    D. 4. oktober 1957, under den kolde krig, sendte sovjet-unionen den første ubemandet satellit i kredsløb, sputnik 1.
                    Dette skabte frygt for ”missile gap” i USA, og for at sikre USA’s teknologiske fordel grundlagde
                    de ”Defense Advanced Research Project Agency” aka. DARPA.
                    På dette tidspunkt blev data overført personligt. DARPA iværksatte et stort computer-netværk, for at fremskynde data-overførsel,
                    og sikre at man ikke lavede dobbelt-research indenfor samme områder. Dette kaldte de ARPANET.
                </p>
                <p class="section__text">
                    Herudover var der 3 vigtige netværk, som også bidrager til historien af det moderne internet.
                </p>
                <ul class="section__list">
                    <li class="list__item">RAND - Militært netværk af Rand Corporation</li>
                    <li class="list__item">NPL - Kommercielt netværk af National Physical Laboratory i England</li>
                    <li class="list__item">CYCLADES - Videnskabeligt netværk i Frankrig</li>
                </ul>
                <p class="section__text">
                    Disse 4 koncepter danner fundamentet af det moderne internet.
                </p>
                <p class="section__text">
                    I 1966 bestod ARPANET af en mainframe og en IMP(Interface Message Processor).
                    IMP’en stod for styring af netværket, og interface for mainframen som kun stod for programmer og data.
                    Disse forskellige IMPs var forbundet i noget man kaldte IMP subnet.
                    Det er også her hvor TCP(Transfer Control Protocol) stammer fra.
                </p>
                <p class="section__text">
                    I og med at NPL skulle bruges til handel forventede man mange overførsler,
                    og for at undgå forbindelserne blev overbelastet, så fandt man på packet-switching.
                    Det betyder at filerne blev delt ind i små pakker ved afsender, og samlet igen ved modtageren.
                </p>
                <p class="section__text">
                    CYCALDES rolle er måske endda mere central i den generelle forståelse af internettet.
                    De var ikke nær så store og havde ikke så stort et budget, som de andre 2.
                    Deres fokus blev derfor lagt på kommunikation med andre netværk.
                    Det var således at udtrykket inter-net kom frem. Inter betyder between(mellem) og net betyder network(netværk).
                    CYCLADES tog dog skridtet videre og implementerede forbindelse direkte i hardwaren, så de var direkte forbindelse.
                </p>
                <p class="section__text">
                    Ydermere udviklede ISO(International Organisation for Standardization) systemet man kalder OSI, Open System Interconnection.
                    Endelig så assimilerede TCP designet fra OSI, og fra det har vi så TPC/IP-protokollen.
                    Det er denne standard som gør alle netværk kompatible med hinanden og ved at sammenlægge dem, har vi så internettet. 
                </p>
