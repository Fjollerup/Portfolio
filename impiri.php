            <div class="section__item">
                <header class="section__header">
                    <h3>Kvantitativ Metode</h3>
                </header>
                <p class="section__text">
                    Den kvantitative metode er den typiske markedsundersøgelser,
                    som vi oplever når vi bliver afbrudt midt i maden af et opkald,
                    hvor man lige vil stille nogle spørgsmål, eller når vi udfylder et spørgeskema på nettet.
                </p>
                <p class="section__text">
                    Disse har til formål at samle overfladiske data. De handler typisk om hvor mange,
                    og ikke hvorfor, mennesker gør noget bestemt. Prøver man grave dybere med den kvantitative metode,
                    kommer det oftest i forudindstillet valgmuligheder, som i sidste ende ikke gør den store forskel.
                </p>
                <h4>Styrker</h4>
                <p class="section__text">
                    Styrkerne ved den kvantitative metode er at man kommer vidt omkring,
                    og man får meget omfattende mængde data over mange mennesker.
                </p>
                <h4>Svagheder</h4>
                <p class="section__text">
                    Svaghederne derimod er at man ikke formår at grave dybere
                    og få en forståelse for hvorfor brugeren gør som de gør, og det er svært at tilpasse oplevelsen.
                </p>
                <h4>Eksempel</h4>
                <p class="section__text">
                    Når man har ringet til TDC, og man efterfølgende bliver spurgt om man vil deltage i en undersøgelse, eller om support-medarbejderen må stille en rækkespørgsmål inden man ligger på.
                </p>
                <p class="section__text">
                    Disse er ofte noget lignende - på en skala fra 1-10:
                </p>
                <ul class="section__list">
                    <li class="list__item">Hvor professionel synes du medarbejderen var</li>
                    <li class="list__item">Hvor godt forstod medarbejderen dit problem</li>
                    <li class="list__item">Hvor godt forstod medarbejderen din grund til at kontakte support</li>
                    <li class="list__item">Hvordan var ventetiden, indne du kom igennem</li>
                    <li class="list__item">Hvor sandsynligt er det, at du vil anbefale TDC til dem du kender</li>
                    <li class="list__item">Hvor sandsynligt er det, at du vil...</li>
                </ul>
                <p class="section__text">
                    Her kommer man frem til statistikker, og kolde data. Der findes ikke noget hvorfor.
                    I dette eksempel ville formålet nærmere være at finde ud af ”er vores kunder tilfredse”,
                    ”er der grundlag for at ændre noget?”. Er der så et grundlag, så kunne man
