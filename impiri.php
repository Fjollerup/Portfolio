        <section class="section" id="mening">
            <div class="section__item">
                <header class="section__header">
                    <h3>Meningskondensering</h3>
                </header>
                <p class="section__text">
                    Meningskondensering er baseret på fortolkende fænomenologisk analysemetode,
                    som er en metode til at analysere kvalitative undersøgelser. Kort fortalt
                    handler det om at skabe indsigt i hvordan en bestemt person, i en bestemt
                    sammenhæng, skaber betydning for et bestemt emne.
                </p>
                <p class="section__text">
                    Meningskondensering er en metode, hvorpå man finder frem til de rette data.
                    Når man analyserer et interview, fjerner man al off-topic snak og det,
                    som ikke tjener noget formål for emnet og hvorfor man interviewer den person.
                </p>
                <p class="section__text">
                    Når man har fat i den data man skal bruge, så omformulerer man
                    det til en kortere og mere præcis form.
                </p>
                <p class="section__text">
                    Man kan vel næsten sige at man ”kondensere” sin data ned til
                    essensen af det, som det handler om.
                </p>
