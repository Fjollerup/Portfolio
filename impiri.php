        <section class="section" id="kvalitativ">
            <div class="section__item">
                <header class="section__header">
                    <h3>Kvalitativ Metode</h3>
                </header>
                <p class="section__text">
                    Denne metode handler udelukkende om at grave dybere, og forstår hvorfor den enkelte person gør som de gør.
                    Derfor bliver den kvalitative undersøgelser ofte til det vi kalder et interview,
                    som er langt mere personligt og nærgående.
                </p>
                <blockquote class="section__quote" cite="Jeffrey Zeldman">
                    <p>”målet er at kunne besvare, hvorfor personen handler, som han gør.
                        Ordet ”interview” betyder netop et kig ind i et andet menneskes indre”</p>
                    <footer> - Anne Mette Busch</footer>
                </blockquote>
                <p class="section__text">
                    I den kvalitative metode benytter man sig af en række trin,
                    som hjælper med at skabe den bedste oplevelse, får både interviewer,
                    men også interviewede.
                </p>
                <p class="section__text">
                    Det er vigtigt, at man gør sig en række overvejelser inden man går i gang.
                    Først skal man vide hvad man ønsker at undersøge og hvorfor, så er det nemmere
                    at finde formål og emne for det interview man skal foretage.
                    Med det på plads kan man begynde at kigge på hvordan man ønsker at finde sit svar.
                </p>
                <p class="section__text">
                    Når alt kommer til alt, er den kvalitative metode mere tilrettelagt og den er designet specifikt baseret på,
                    hvad man ønsker at opnå, men også hvem man skal interview.
                </p>
                <h4>Svagheder</h4>
                <p class="section__text">
                    Svagheden er at det er svært at nå ud til rigtig mange, på kort tid. Det tager rigtig meget tid at nå ud til rigtig mange mennesker.
                    Derfor er det oftest fokusgrupper eller særlige interviews der foretages.
                </p>
                <h4>Styrker</h4>
                <p class="section__text">
                    Den kvalitative metodes styrke er at man kommer rigtig meget i dybden,
                    og kommer frem til nogle meget specifikke svar,
                    og til tider får man også nogle nye erkendelser om enten emne eller interviewede.
                </p>
                <h4>Eksempel</h4>
                <s class="section__text">
                    Her går man som sagt i dybden. Hvis man f.eks. skulle interview
                    Robert Downey Jr. Omkring hans rolle som ”Tony Stark / Iron Man”
                    både iron man og avengers filmene.
                </s>
                <s class="section__text">
                    Man ville spørge ind til hans handlinger, og ikke så meget hans holdninger.
                </s>
                <ul class="section__list">
                    <li class="list__item">Alle ved du improviserede i denne scene, hvorfor valgte du at gøre sådan?</li>
                    <li class="list__item">I dag har vi CGI og green screen, har du prøvet at have en real-size suit på?</li>
                    <li class="list__item list__item--indent">Hvordan føles det? Jeg forestiller mig den er pænt tung! "haha"</li>
                    <li class="list__item">Hvordan kom du frem til denne fortolkning af Tony Stark?</li>
                    <li class="list__item list__item--indent">Har du læst tegneserierne?</li>
                    <li class="list__item list__item--indent">Hvad synes du?</li>
                    <li class="list__item list__item--indent">Hvad gjorde du for at forberede dig til rollen i Iron Man 3?</li>
                    <li class="list__item">Hvordan forbereder du dig til de efterfølgende film?</li>
                    <li class="list__item list__item--indent">Følger du med på social media, for at forstå hvad Iron Man fans synes om din optræden?</li>
                    <li class="list__item">Forstår du noget af alt det, som Tony Stark snakker om i filmene, eller er det bare replikker?</li>
                    <li class="list__item">Hvilken Iron Man suit er din favorit?</li>
                    <li class="list__item list__item--indent">Interessant, kan du fortælle hvorfor?</li>
                </ul>
                <p class="section__text">
                    Det er helt fint at spørge ind til holdninger, men man skal passe lidt på da den
                    interviewede godt kan blive selv for rationelle og selvbevidste.
                    Er interviewet designet rigtig, med de rigtige spørgsmål, hvor interviewede
                    føler sig tryg og tilpas, så kan man fortolke interviewedes holdninger ud fra deres handlinger.
                </p>
                <ul class="section__kilde">
                    <li><a class="kilde__link" href="">Kommunikation og multimediedesign, s.88-89</a></li>
