        <section class="section" id="transkribere">
            <div class="section__item">
                <header class="section__header">
                    <h3>Transkribering</h3>
                </header>
                <p class="section__text">
                    I ordets reneste form betyder det at ”overføre til skrevet form”.
                    Transskribere kommer fra det latinske <i>transcribere</i> og betyder
                    ”at kopiere, skrive over, overføre”
                    – med dette kan man sige at det er rimelig selvforklarende.
                </p>
                <p class="section__text">
                    I forbindelse med undersøgelser, så betyder det simpelthen
                    at få skrevet et interview ned, eller overført data til tekst.
                </p>
                <h4>Eksempel: Kvantitativ metode</h4>
                <p class="section__text">
                    Med 200 spørgeskemaer skal man have dem ”overskrevet” til en mere forståelig form,
                    så man samler al data fra disse spørgeskemaer og skriver det ned, så det kan bruges.
                    I dag behøver man ikke nødvendigvis at transskribere spørgeskemaer, da det bliver
                    mere og mere udbredt at disse foregår på nettet, og disse vil automatisk ”transskribere”
                    den indsamlede data.
                </p>
                <h4>Eksempel: Kvalitativ metode</h4>
                <p class="section__text">
                    Har man f.eks. optaget et interview på en diktafon, eller filmet det, så transskribere
                    man interviewet når man overføre det til papirform, hvor det kan bruges til en artikel.
                </p>
                <ul class="section__kilde">
                    <li><a class="kilde__link" href="">Kommunikation og multimediedesign, s.92</a></li>
                </ul>
            </div>
